package com.wustrive.util.net;

import java.io.File;

public class FileParameter {

	private String name;

	private File file;

	private byte[] data;

	private String fileName;

	public FileParameter(String name, byte[] data, String fileName) {
		this.name = name;
		this.data = data;
		this.fileName = fileName;
	}

	public FileParameter(String name, File file) {
		this.name = name;
		this.file = file;
	}

	public String getFileName() {
		return fileName;
	}

	public byte[] getData() {
		return data;
	}

	public String getName() {
		return name;
	}

	public File getFile() {
		return file;
	}
}